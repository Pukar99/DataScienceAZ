# OOP in Python
# To map with the real world, we use OOPs concepts.
# Class: Blueprint of an object.
# Object: Instance of a class.
# Method: Function in a class.

# Class
class Student:
    name = "Pukar Sharma"
    age = 21
    stream = "Computer Science"
    
s1 = Student() # Creating Object of Student Class
print(s1)
print(s1.name)
print(s1.age)
print(s1.stream)


class Car:
    color = "Black"
    brand = "BMW"
car1 = Car()
print(car1.color)
print(car1.brand)

