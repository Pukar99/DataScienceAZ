#arithmetic operators
a = 10
b = 20

sum = a + b
print(sum)
# Output: 30

sub = a - b
print(sub)
# Output: -10

mul = a * b
print(mul)
# Output: 200

div = a / b
print(div)
# Output: 0.5

mod = a % b
print(mod)
# Output: 10

exp = a ** 3
print(exp)
# Output: 1000

#Relational Operators
a = 10
b = 20
print(a == b)
# Output: False
print(a != b)
# Output: True
print(a > b)
# Output: False
print(a < b)
# Output: True
print(a >= b)
# Output: False
print(a <= b)
# Output: True



#Assignment Operators
a = 10
b = 20
a += b
print(a)
# Output: 30
a -= b
print(a)
# Output: 10
a *= 3
print(a)
# Output: 30



#Logical Operators
a = True
b = False
print(a and b)
# Output: False
print(a or b)
# Output: True
print(not a)
# Output: False
print(not b)
